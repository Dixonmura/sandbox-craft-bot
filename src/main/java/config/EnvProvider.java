package config;

/**
 * Абстракция над источником переменных окружения.
 * Позволяет получить значение по имени.
 */
public interface EnvProvider {

    /**
     * Возвращает значение переменной окружения по её имени.
     *
     * @param name имя переменной окружения
     * @return значение переменной или null, если не найдено
     */
    String getEnv(String name);
}
